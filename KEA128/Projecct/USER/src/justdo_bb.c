#include "headfile.h"

/**************变量**************/
uint16_t bbtime = 0;

/**************函数**************/
void bb_init()
{
  gpio_init(H5,GPO,0);
}
void justdo_bb()
{
  if(bbtime>0)
  {
    gpio_set(H5,1);
    bbtime--;
  }
  else
    gpio_set(H5,0);
}
